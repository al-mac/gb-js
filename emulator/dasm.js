	var startAt = 0;
	var ran = 0;
	var format = "{asm}{20}; pc: 0x{p.pc.toString(16).toUpperCase()}";
	var ignore = [];
	
	me.step = function(pc, p) {
		ran++;
		if(ran < startAt) return;
		if(trace <= 0) return;
		trace--;
		var res = "";
		var op = sys.m.getAddr(pc);
		
		if(ignore.indexOf(op) > -1)
			return;
		
		var map = op === 0xCB ? cb : un;
		op = op === 0xCB ? sys.m.getAddr(pc + 1) : op;
		res = map[op](op).trim();
		
		var s1 = sys.m.getAddr(pc + 1).toString(16).padStart(2, "0").toUpperCase();
		var s2 = sys.m.getAddr(pc + 2).toString(16).padStart(2, "0").toUpperCase();
		res = res.replace("d16", "0x" + s2 + s1);
		res = res.replace("a16", "0x" + s2 + s1);
		res = res.replace("d8", "0x" + s1);
		res = res.replace("a8", "0xFF00 + 0x" +  s1);
		res = res.replace("a8", "0xFF00 + 0x" +  s1);
		res = res.replace("(C)", "(0xFF00 + C)");
		res = res.replace("r8", "["+ s1 +"]");
		
		return parseFormat(res, p);
	};
	
	var parseFormat = function(asm, p) {
		var result = "";
		var expression = "";
		var getting = false;
		for(var i = 0; i < format.length; i++) {
			if(format[i] === "{") {
				getting = true;
				continue;
			};
			
			if(format[i] === "}") {
				getting = false;
				var expv = eval(expression);
				if(isNaN(expression))
					result += expv;
				else {
					while(result.length < expv)
						result += "|";
				};
				expression = "";
				continue;
			};
			
			if(getting) {
				expression += format[i];
				continue;
			}
			
			result += format[i];
		};
		

		while(result.indexOf("|") > -1)
			result = result.replace("|", "&nbsp;");
		
		return result;
	};
	
